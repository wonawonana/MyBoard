public class FavoriteCafeLocation extends Fragment implements OnMapReadyCallback {
    ViewGroup viewGroup;
    double cafeY, cafeX;

    FavoriteCafeLocation(double cafeY, double cafeX){
        this.cafeY=cafeY;
        this.cafeX=cafeX;
    }

    @RequiresApi(api = Build.VERSION_CODES.LOLLIPOP)
    @Nullable
    @Override
    public View onCreateView(@NonNull LayoutInflater inflater, @Nullable ViewGroup container, @Nullable Bundle savedInstanceState)
    {

        //지도 xml
        viewGroup = (ViewGroup) inflater.inflate(R.layout.map,container,false);


        //네이버지도
        FragmentManager fm = getChildFragmentManager();
        MapFragment mapFragment = (MapFragment)fm.findFragmentById(R.id.map);
        if (mapFragment == null) {
            mapFragment = MapFragment.newInstance();
            fm.beginTransaction().add(R.id.map, mapFragment).commit();
        }
        mapFragment.getMapAsync(this);



        return viewGroup;
    }
    @Override
    public void onMapReady(@NonNull NaverMap naverMap) {
        CameraUpdate cameraUpdate = CameraUpdate.scrollTo(new LatLng(cafeY, cafeX));
        naverMap.moveCamera(cameraUpdate);
    }
}